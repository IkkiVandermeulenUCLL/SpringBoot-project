DROP TABLE IF EXISTS ANIMALS_TOYS;
DROP TABLE IF EXISTS MY_MEDICALRECORDS;
DROP TABLE IF EXISTS MY_ANIMALS;
DROP TABLE IF EXISTS MY_TOYS;
DROP TABLE IF EXISTS MY_STABLES;
DROP TABLE IF EXISTS MY_ADDRESSES;

CREATE TABLE MY_ADDRESSES(
    ID          BIGINT AUTO_INCREMENT PRIMARY KEY,
    STREET      VARCHAR(255),
    NUMBER      INT,
    PLACE       VARCHAR(255)
);

CREATE TABLE MY_STABLES(
    ID          BIGINT AUTO_INCREMENT PRIMARY KEY,
    NAME        VARCHAR(255),
    CAPACITY    INT,
    ADDRESS_ID  BIGINT,
    FOREIGN KEY (ADDRESS_ID) REFERENCES MY_ADDRESSES
);

CREATE TABLE MY_ANIMALS(
    ID          BIGINT AUTO_INCREMENT PRIMARY KEY,
    NAME        VARCHAR(255),
    AGE         INT,
    STABLE_ID   BIGINT,
    FOREIGN KEY (STABLE_ID) REFERENCES MY_STABLES
);

CREATE TABLE MY_MEDICALRECORDS(
    ID                  BIGINT AUTO_INCREMENT PRIMARY KEY,
    REGISTRATION_DATE   DATE,
    CLOSING_DATE        DATE,
    DESCRIPTION         VARCHAR(255),
    ANIMAL_ID           BIGINT,
    FOREIGN KEY (ANIMAL_ID) REFERENCES MY_ANIMALS
);

CREATE TABLE MY_TOYS(
    ID          BIGINT AUTO_INCREMENT PRIMARY KEY,
    NAME        VARCHAR(255)
);

CREATE TABLE ANIMALS_TOYS(
    ID          BIGINT AUTO_INCREMENT PRIMARY KEY,
    ANIMAL_ID   BIGINT,
    TOY_ID      BIGINT,
    FOREIGN KEY (ANIMAL_ID) REFERENCES MY_ANIMALS,
    FOREIGN KEY (TOY_ID) REFERENCES MY_TOYS
);

